public class explicit_typecasting {
    public static void main(String[] args) {
        byte b = 127;
        int a = 257;
        byte k = (byte) a;

        float f = 5.6f;
        int t = (int) f;
        byte a1 = 10;
        byte b1 = 30;
        int result = a1 * b1;
        System.out.println(result);
    }
}
